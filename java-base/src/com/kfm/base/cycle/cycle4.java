package com.kfm.base.cycle;

public class cycle4 {
    public static void main(String[] args) {
        for (int i = 1 ;i <= 9;i++){


            for (int j = 1;j <= 9;j++){
                System.out.print(i + "*" +j + "=" + i * j);

            }
            System.out.println();
        }
        System.out.println();
    }

}
